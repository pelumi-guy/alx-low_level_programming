#include <stdio.h>

/**
 * add - function to add 2 numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 **/
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to add 2 numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 **/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function to add 2 numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 **/
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function to add 2 numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 **/
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function to add 2 numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 **/
int mod(int a, int b)
{
	return (a % b);
}
